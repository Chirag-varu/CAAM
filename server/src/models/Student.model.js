import { Schema, model } from "mongoose";

const studentSchema = new Schema({
  email: {
    type: String,
    required: true,
    unique: true,
    match: /.+\@.+\..+/,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
  name: {
    type: String,
    required: true,
  },
});

const Student = model("Student", studentSchema);

export default Student;
